    private class MyClosingWindow extends WindowAdapter {

        @Override
        public void windowClosing(WindowEvent e) {
            int r = JOptionPane.showConfirmDialog(Main.this, "Are you sure?", "Confirm", JOptionPane.OK_CANCEL_OPTION);
            if (r == JOptionPane.OK_OPTION) {
                Main.this.dispose();
                System.exit(0);
            }
        }
    }

