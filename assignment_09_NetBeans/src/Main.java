    private void jButtonWestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonWestActionPerformed
        if (jButtonWest.getText().equals("Go")) {
            jButtonWest.setText("Suspend");
            if (!(threadWest.isAlive())) {
                threadWest.start();
            }
            else 
                threadWest.resumeThread();
        } else {
            jButtonWest.setText("Go");
            threadWest.stopThread(); 
        }
    }//GEN-LAST:event_jButtonWestActionPerformed

